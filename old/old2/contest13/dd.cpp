int n, k;
const int N = 2*1e5+7;
vi arr(N);

bool check(int x){
	int s = 0;
	for(int i=1; i <= N; i++){
		s += arr[i] / x;
	}
	return s >= k;
}
void solve(){
	cin >> n >> k;
	For(i, n){
		int x;
		cin >> x;
		arr[x]++;
	}
	int l = 1, r = n;
	while(l < r){
		int mid = (l + r + 1) / 2;
		if(check(mid))
			l = mid;
		else
			r = mid - 1;
	}
	vi ans;
	for(int i=1; i < N; i++){
		for(int j=1; j <= arr[i] / r; ++j){
			if(ans.size() < k)
				ans.push_back(i);
		}
	}
	for(auto i : ans)
		cout << i << " ";
	cout << endl;
}

