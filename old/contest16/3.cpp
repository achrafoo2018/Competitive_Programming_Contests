#define fastio ios_base::sync_with_stdio(false)
#define debug(x) cerr << #x << " is " << x << "\n"
#define all(v) v.begin(), v.end()
#define rall(v) v.rbegin(), v.rend()
#define For(i, n) for(int i=0; i < n; ++i)

void solve() {
    ll n,h;
    cin >> n >> h;
    vector<ll> a(n);
    for(ll i=0;i<n;i++)
    	cin >> a[i];
    vector<ll> maxd(n);
    ll maxdamage = 0,sum = 0;
    for(ll i=0;i<n-1;i++){
        maxd[i] = a[i+1]-a[i];
        maxdamage = max(maxd[i], maxdamage);
        sum += maxd[i];
    }
    maxd[n-1] = INT64_MAX;
    if((h - sum) >= maxdamage) {
        cout << h - sum << endl;
        return;
    }
    ll x = 1;
    ll ans = maxdamage;
    while(x <= maxdamage) {
        ll mid = (x + maxdamage)/2;
        sum = 0;
        for(ll i=0;i < n;i++) 
            sum += min(mid, maxd[i]);
        if(sum >= h) {
            ans = min(mid, ans);
            maxdamage = mid-1;
        }
        else 
            x = mid+1;
        
    }
    cout<<ans<< endl;
}

int main() {
	fastio;
	int t;
    cin >> t;
	while(t--)
        solve();
}