bool good(ll r){
	return (r / w) * (r / h) >= n;
}

void solve(){
	cin >> w >> h >> n;
	ll l = 0, r = 1;
	while(!good(r))
		r *= 2;
	ll ans = r;
	while(l <= r){
		ll mid = (l+r)/2;
		ll x = (mid/w) * (mid /h);
		if(x >= n){
			ans = min(ans, mid);
			r = mid-1;
		}else{
			l = mid+1;
		}
	}
	cout << ans << endl;
}

